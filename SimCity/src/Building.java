
public abstract class Building {
	protected String Name;
	protected String address;
	
	/**
	* This method getName has no paramaters 
	* @return Name
	*/
	
	public String getName()
	{
		return Name;
	}
	/**
	* This method getAddress has no paramaters 
	* @return address
	*/
	
	public String getAddress()
	{
		return address;
	}
}